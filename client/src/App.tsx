import { Toaster } from "@/components/ui/sonner";
import { TooltipProvider } from "@/components/ui/tooltip";
import NotFound from "@/pages/NotFound";
import { Route, Switch } from "wouter";
import ErrorBoundary from "./components/ErrorBoundary";
import { ThemeProvider } from "./contexts/ThemeContext";
import Home from "./pages/Home";
import Landing from "./pages/Landing";
import Permissions from "./pages/Permissions";
import PermissionDetail from "./pages/PermissionDetail";
import ActivityLog from "./pages/ActivityLog";
import ProfileSelection from "./pages/ProfileSelection";
import CalculatorPublic from "./pages/CalculatorPublic";

function Router() {
  // make sure to consider if you need authentication for certain routes
  return (
    <Switch>
      <Route path={"/"} component={Landing} />
      <Route path={"/profile-selection"} component={ProfileSelection} />
      <Route path={"/calculateur"} component={CalculatorPublic} />
      <Route path={"/app"} component={Home} />
      <Route path={"/permissions"} component={Permissions} />
      <Route path={"/permissions/activity"} component={ActivityLog} />
      <Route path={"/permissions/:category"} component={PermissionDetail} />
      <Route path={"/404"} component={NotFound} />
      {/* Final fallback route */}
      <Route component={NotFound} />
    </Switch>
  );
}

// NOTE: About Theme
// - First choose a default theme according to your design style (dark or light bg), than change color palette in index.css
//   to keep consistent foreground/background color across components
// - If you want to make theme switchable, pass `switchable` ThemeProvider and use `useTheme` hook

function App() {
  return (
    <ErrorBoundary>
      <ThemeProvider
        defaultTheme="light"
        // switchable
      >
        <TooltipProvider>
          <Toaster />
          <Router />
        </TooltipProvider>
      </ThemeProvider>
    </ErrorBoundary>
  );
}

export default App;
